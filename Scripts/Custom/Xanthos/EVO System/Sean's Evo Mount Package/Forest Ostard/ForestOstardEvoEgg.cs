using System;
using Server;
using Server.Items;
using Server.Mobiles;
using Xanthos.Interfaces;

namespace Xanthos.Evo
{
	public class ForestOstardEvoEgg : BaseEvoEgg
	{
		public override IEvoCreature GetEvoCreature()
		{
			return new EvoForestOstard( "a Forest Ostard hatchling" );
		}

		[Constructable]
		public ForestOstardEvoEgg() : base()
		{
			Name = "a Forest Ostard egg";
			HatchDuration = 0.01;		// 15 minutes
		}

		public ForestOstardEvoEgg( Serial serial ) : base ( serial )
		{
		}

		public override void Serialize( GenericWriter writer )
		{
			base.Serialize( writer );
			writer.Write( (int)0 );
		}

		public override void Deserialize( GenericReader reader )
		{
			base.Deserialize( reader );
			int version = reader.ReadInt();
		}
	}
}